    # --->>> –ü–†–ê–í–ò–õ–¨–ù–´–ï –û–¢–î–ï–õ–¨–ù–´–ï –û–ë–†–ê–ë–û–¢–ß–ò–ö–ò –î–õ–Ø store_message <<<---
