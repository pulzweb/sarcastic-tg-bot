    for task in done:
        logger.info(f"–ü—Ä–æ–≤–µ—Ä–∫–∞ –∑–∞–≤–µ—Ä—à–µ–Ω–Ω–æ–π –∑–∞–¥–∞—á–∏: {task.get_name()}")
        try:
            # –ï—Å–ª–∏ –≤ –∑–∞–¥–∞—á–µ –±—ã–ª–æ –∏—Å–∫–ª—é—á–µ–Ω–∏–µ, –æ–Ω–æ –ø–æ–¥–Ω–∏–º–µ—Ç—Å—è –∑–¥–µ—Å—å
            await task
        except asyncio.CancelledError:
             logger.info(f"–ó–∞–¥–∞—á–∞ {task.get_name()} –±—ã–ª–∞ –æ—Ç–º–µ–Ω–µ–Ω–∞.")
